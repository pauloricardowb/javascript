//loop com while
var c = 1
while (c <= 6) {
    console.log(`teste ${c}`)
    c++
}

console.log('----------')

//loop com do while
var ca = 1

do {
    console.log(`teste ${ca}`)
    ca++
} while (ca <= 6)

console.log('----------')

//loop com for

for (var c = 1;c <= 6;c ++){
    console.log(`teste ${c}`)
}
